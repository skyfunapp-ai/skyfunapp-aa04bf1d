// Pre-compute rows: each category produces a header row + N emoji rows
type Row =
  | { type: "header"; label: string }
  | { type: "emojis"; items: string[] };

function buildRows(categories: Category[], cols: number): Row[] {
  const rows: Row[] = [];
  for (const cat of categories) {
    rows.push({ type: "header", label: cat.label });
    for (let i = 0; i < cat.emojis.length; i += cols) {
      rows.push({ type: "emojis", items: cat.emojis.slice(i, i + cols) });
    }
  }
  return rows;
}

interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
}

const EmojiPicker: React.FC<EmojiPickerProps> = React.memo(({ onSelect }) => {
  const parentRef = useRef<HTMLDivElement>(null);
  const [activeTab, setActiveTab] = useState(0);

  const rows = useMemo(() => buildRows(CATEGORIES, COLS), []);

  const rowVirtualizer = useVirtualizer({
    count: rows.length,
    getScrollElement: () => parentRef.current,
    estimateSize: (i) => rows[i].type === "header" ? HEADER_HEIGHT : EMOJI_SIZE + GAP,
    overscan: 8,
  });

  const scrollToCategory = useCallback((idx: number) => {
    setActiveTab(idx);
    let rowIdx = 0;
    for (let i = 0; i < idx; i++) {
      rowIdx += 1 + Math.ceil(CATEGORIES[i].emojis.length / COLS);
    }
    rowVirtualizer.scrollToIndex(rowIdx, { align: "start" });
  }, [rowVirtualizer]);

  const tabLabels = ["😀","👋","🐶","🍕","🚗","⚽","❤️","🏁"];

  return (
    <div className="mb-2 bg-card border border-border rounded-xl overflow-hidden">
      {/* Category tabs */}
      <div className="flex border-b border-border/30 px-1">
        {tabLabels.map((icon, i) => (
          <button
            key={i}
            type="button"
            onClick={() => scrollToCategory(i)}
            className={`flex-1 py-1.5 text-base transition-colors ${activeTab === i ? "bg-muted" : "hover:bg-muted/50"} rounded-t-md`}
          >
            {icon}
          </button>
        ))}
      </div>
      {/* Virtualized grid */}
      <div
        ref={parentRef}
        className="overflow-y-auto"
        style={{ height: 200, WebkitOverflowScrolling: "touch" }}
      >
        <div style={{ height: rowVirtualizer.getTotalSize(), position: "relative" }}>
          {rowVirtualizer.getVirtualItems().map((virtualRow) => {
            const row = rows[virtualRow.index];
            return (
              <div
                key={virtualRow.index}
                style={{
                  position: "absolute",
                  top: 0,
                  left: 0,
                  width: "100%",
                  height: virtualRow.size,
                  transform: `translateY(${virtualRow.start}px)`,
                }}
              >
                {row.type === "header" ? (
                  <div className="text-[10px] text-muted-foreground px-2 pt-1 font-medium">
                    {row.label}
                  </div>
                ) : (
                  <div className="flex gap-1 px-1">
                    {row.items.map((emoji) => (
                      <button
                        key={emoji}
                        type="button"
                        onClick={() => onSelect(emoji)}
                        className="text-xl w-9 h-9 flex items-center justify-center hover:bg-muted rounded"
                      >
                        {emoji}
                      </button>
                    ))}
                  </div>
                )}
              </div>
            );
          })}
        </div>
      </div>
    </div>
  );
});

EmojiPicker.displayName = "EmojiPicker";

export default EmojiPicker;
